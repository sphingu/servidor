Cypress.Commands.add('requestAPI', (query: string): any => {
  return cy.request({
    url: '/',
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: {
      query,
    },
    failOnStatusCode: false,
  })
})
